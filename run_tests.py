def run_command(cmd: str, description: str) -> bool:
    """
    –ó–∞–ø—É—Å–∫ –∫–æ–º–∞–Ω–¥—ã —Å –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ–º.

    Args:
        cmd: –ö–æ–º–∞–Ω–¥–∞ –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è
        description: –û–ø–∏—Å–∞–Ω–∏–µ –∫–æ–º–∞–Ω–¥—ã

    Returns:
        True –µ—Å–ª–∏ –∫–æ–º–∞–Ω–¥–∞ –≤—ã–ø–æ–ª–Ω–µ–Ω–∞ —É—Å–ø–µ—à–Ω–æ
    """
    print(f"\nüîÑ {description}")
    print(f"–í—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è: {cmd}")

    try:
        result = subprocess.run(cmd, shell=True, capture_output=True, text=True)

        if result.returncode == 0:
            print(f"‚úÖ {description} - –£–°–ü–ï–•")
            if result.stdout:
                print(f"–í—ã–≤–æ–¥: {result.stdout.strip()}")
            return True
        else:
            print(f"‚ùå {description} - –û–®–ò–ë–ö–ê")
            print(f"–ö–æ–¥ –æ—à–∏–±–∫–∏: {result.returncode}")
            if result.stderr:
                print(f"–û—à–∏–±–∫–∞: {result.stderr.strip()}")
            return False

    except Exception as e:
        print(f"‚ùå {description} - –ò–°–ö–õ–Æ–ß–ï–ù–ò–ï: {e}")
        return False


